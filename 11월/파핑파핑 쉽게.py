
di = [-1, -1, -1, 0, 1, 1, 1, 0]
dj = [-1, 0, 1, 1, 1, 0, -1, -1 ]




T = int(input())
for t in range(1, T+1):
    n = int(input())
    mat = [list(input()) for _ in range(n)]

    cnt_no_bumb = 0
    for i in range(n):
        for j in range(n):
            if mat[i][j] == '.':
                mat[i][j] = 0
            elif mat[i][j] == '*':
                for k in range(8):
                    ii, jj = i + di[k], j + dj[k]
                    if 0<= ii < n and 0 <= jj < n:
                        if mat[ii][jj] != '*':
                            mat[ii][jj] = 1

    for a in mat:
        print(a)

